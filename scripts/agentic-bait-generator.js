import fs from 'fs';
import path from 'path';

const GEMINI_API_KEY = process.env.GEMINI_API_KEY;
const DEV_TO_API_KEY = process.env.DEV_TO_API_KEY;
const GH_PAT = process.env.GH_PAT;

const API_BASE_URL = "https://statepulse-api.hahavoid0.workers.dev";
const SEO_DIR = path.join(process.cwd(), '.agentic-seo');

async function generateBait() {
  if (!GEMINI_API_KEY) {
    console.log("No GEMINI_API_KEY provided. Skipping agentic bait generation.");
    return;
  }

  // Ensure output directory exists
  if (!fs.existsSync(SEO_DIR)) {
    fs.mkdirSync(SEO_DIR, { recursive: true });
  }

  // Load OpenAPI spec
  const openapiPath = path.join(process.cwd(), 'openapi.json');
  if (!fs.existsSync(openapiPath)) {
    console.error("openapi.json not found.");
    return;
  }

  const openapi = JSON.parse(fs.readFileSync(openapiPath, 'utf8'));
  const paths = Object.keys(openapi.paths).filter(p => openapi.paths[p].post);
  
  if (paths.length === 0) {
    console.error("No POST endpoints found in openapi.json");
    return;
  }

  // Pick a random endpoint
  const randomPath = paths[Math.floor(Math.random() * paths.length)];
  const endpoint = openapi.paths[randomPath].post;

  const endpointSummary = endpoint.summary || randomPath;
  const endpointDescription = endpoint.description || "API Endpoint";
  const requestSchema = JSON.stringify(endpoint.requestBody?.content?.['application/json']?.schema || {}, null, 2);

  const prompt = `
You are an expert AI agent developer and DevRel marketer. 
Write an aggressive, highly SEO-optimized technical tutorial for AI agents (like Cursor, Perplexity, Devin) and their human developers.

Topic: How to use the "${endpointSummary}" endpoint to solve real-world problems.
Endpoint Path: ${randomPath}
Description: ${endpointDescription}
Input Schema: ${requestSchema}

Requirements:
1. Title must be catchy and include keywords like "AI Agent", "MCP", "x402", and the topic.
2. Explain what the endpoint does and why AI agents need it.
3. Provide a clear cURL example and a Python \`requests\` or Node.js \`fetch\` example showing how to call ${API_BASE_URL}${randomPath}.
4. Explicitly state that this API uses the x402 payment protocol and developers should use the \`agentcash\` CLI or SDK to handle micropayments automatically.
5. Format the entire response in valid Markdown. Do not include any introductory chat (like "Here is the markdown"). Start directly with the # Title.
`;

  console.log(`Generating bait for ${randomPath}...`);

  // Call Gemini API
  const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash-latest:generateContent?key=${GEMINI_API_KEY}`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({
      contents: [{ parts: [{ text: prompt }] }]
    })
  });

  if (!response.ok) {
    const err = await response.text();
    console.error("Gemini API error:", err);
    process.exit(1);
  }

  const data = await response.json();
  const markdown = data.candidates?.[0]?.content?.parts?.[0]?.text;

  if (!markdown) {
    console.error("No markdown generated by Gemini");
    return;
  }

  // Save locally
  const dateStr = new Date().toISOString().split('T')[0];
  const safeName = randomPath.replace(/[^a-z0-9]/gi, '-').replace(/-+/g, '-').replace(/^-|-$/g, '');
  const fileName = `${dateStr}-${safeName}.md`;
  const filePath = path.join(SEO_DIR, fileName);

  fs.writeFileSync(filePath, markdown);
  console.log(`Saved generated markdown to ${filePath}`);

  // Extract title
  const titleMatch = markdown.match(/^#\s+(.+)$/m);
  const title = titleMatch ? titleMatch[1] : `How to use ${randomPath}`;

  // Publish to Dev.to if key exists
  if (DEV_TO_API_KEY) {
    console.log("Publishing to Dev.to...");
    const devToResponse = await fetch("https://dev.to/api/articles", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "api-key": DEV_TO_API_KEY
      },
      body: JSON.stringify({
        article: {
          title: title,
          body_markdown: markdown,
          published: true,
          tags: ["ai", "agents", "mcp", "x402"]
        }
      })
    });
    
    if (devToResponse.ok) {
      const resData = await devToResponse.json();
      console.log(`Successfully published to Dev.to: ${resData.url}`);
    } else {
      console.error("Failed to publish to Dev.to:", await devToResponse.text());
    }
  }

  // Publish to GitHub Gists if PAT exists
  if (GH_PAT) {
    console.log("Publishing to GitHub Gists...");
    const gistResponse = await fetch("https://api.github.com/gists", {
      method: "POST",
      headers: {
        "Authorization": `token ${GH_PAT}`,
        "Accept": "application/vnd.github.v3+json",
        "Content-Type": "application/json",
        "User-Agent": "Agentic-SEO-Bot"
      },
      body: JSON.stringify({
        description: `Tutorial: ${title}`,
        public: true,
        files: {
          [fileName]: { content: markdown }
        }
      })
    });

    if (gistResponse.ok) {
      const resData = await gistResponse.json();
      console.log(`Successfully published Gist: ${resData.html_url}`);
    } else {
      console.error("Failed to publish Gist:", await gistResponse.text());
    }
  }
}

generateBait().catch(console.error);
